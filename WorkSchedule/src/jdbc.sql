CREATE SEQUENCE  DIVSEQ  

        START WITH 1

        INCREMENT BY 1; 

        

CREATE SEQUENCE  DEPTSEQ  

        START WITH 1

        INCREMENT BY 1;

        

CREATE SEQUENCE  LOCSEQ  

        START WITH 1

        INCREMENT BY 1;
       




--위치_LOCATION--

CREATE TABLE LOCATION(

LOCID  VARCHAR2(10) NOT NULL PRIMARY KEY,

LOCNAME  VARCHAR2(50),

CHECKYN     VARCHAR2(10)

); 










--사업부_DIVISION--

CREATE TABLE DIVISION ( 

DIVID   VARCHAR2(30),

DIVNAME  VARCHAR2(30),

CHECKYN VARCHAR2(10),

CONSTRAINT  DIVISION_PK PRIMARY KEY(DIVID)

);










--부서_DEPARTMENT--

CREATE TABLE DEPARTMENT(

DEPTID   VARCHAR2(30) ,

DEPTNAME VARCHAR2(30),

DIVID   VARCHAR2(30),

CHECKYN VARCHAR2(10),

CONSTRAINT  DEPARTMENT_PK PRIMARY KEY(DEPTID),

CONSTRAINT DEPARTMENT_FK FOREIGN KEY(DIVID) REFERENCES DIVISION(DIVID)

);





--직원_EMPLOYEE--

CREATE TABLE EMPLOYEE (

EMPID  VARCHAR2(30) NOT NULL,

EMPPWD  VARCHAR2(30) NOT NULL,

EMPNAME  VARCHAR2(30),

EMPPHONE VARCHAR2(30),

EMPMAIL  VARCHAR2(30),

EMPLOC  VARCHAR2(30), 

DEPTID VARCHAR2(10),

EMPIMG VARCHAR2(500) DEFAULT 'defaultimg.png',

CONSTRAINT EMPLOYEE_PK  PRIMARY KEY(EMPID),

CONSTRAINT EMPLOYEE_FK  FOREIGN KEY(DEPTID) REFERENCES DEPARTMENT(DEPTID)

);





--즐겨찾기_FAVORITE--

CREATE TABLE FAVORITE(

EMPID  VARCHAR2(30) NOT NULL,

EMPIDFAV VARCHAR2(30) NOT NULL,

CONSTRAINT  FAVORITE_PK PRIMARY KEY(EMPID, EMPIDFAV),

CONSTRAINT FAVORITE_FK FOREIGN KEY(EMPIDFAV) REFERENCES EMPLOYEE(EMPID)

);





--근무_WORK--

CREATE TABLE WORK(

WORKDATE DATE NOT NULL,

EMPID  VARCHAR2(30) NOT NULL,

AMLOC  VARCHAR2(50),

AMLOCDETAIL VARCHAR2(100),

PMLOC  VARCHAR2(50),

PMLOCDETAIL VARCHAR2(100),

CONSTRAINT WORK_PK PRIMARY KEY (WORKDATE, EMPID),

CONSTRAINT WORK_FK FOREIGN KEY(EMPID) REFERENCES EMPLOYEE(EMPID)

);

